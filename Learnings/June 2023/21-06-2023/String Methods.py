# A string
name = "deeznuts"

# Length (Number of Characters)
print(len(name))

# Find Character
print(name.find("z"))

# Capitalize first character
print(name.capitalize())

# Uppercase "Name"
print(name.upper())

# Lower "Name"
print(name.lower())

# IsDigit? (Is "Name" a/a lot of number/s?)
print(name.isdigit())

# IsAlpha? (Does "Name" contain only alphabetical letters?)
print(name.isalpha())

# Count number of specific characters
print(name.count("e"))

# Replace Characters
print(name.replace("d","h"))

# Display "Name" a lot of times
print(name*2)

