# If Statement = a block of code that will execute if its condition is true

Number = int(input("What's your favorite number? "))

if Number == 69:
    print("You are nice")
elif Number < 0:
    print("How did this come to your mind?")
elif Number >= 100:
    print("Ohh, Big Number")
else:
    print("I mean it's alright...")

