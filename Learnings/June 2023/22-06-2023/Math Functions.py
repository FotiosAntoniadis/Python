import math

pi = 3.14

# Round a number
print(round(pi))

# Round a number up
print(math.ceil(pi))

# Round a number down
print(math.floor(pi))

# Absolute Value (Length from 0)
print(abs(pi))

# Pow (Raise to a power)
print(pow(pi, 2))

# Square Root
print(math.sqrt(pi))

x = 1
y = 2
z = 3

# Max (Largest of values)
print(max(x, y, z))

# Min (Lowest of values)
print(min(x, y, z))

