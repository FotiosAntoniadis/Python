#Multiple Assignment = Allows us to assign multiple variables at the same time in one line of code.

#Previously used:
# Name = "DeezNuts"
# FavNumber = 69
# Goofy_Ahh = True

#Now, with multiple assignments:
Name, FavNumber, Goofy_Ahh = "DeezNuts", 69, True
#The same thing, by faster.

print(Name)
print(FavNumber)
print(Goofy_Ahh)

