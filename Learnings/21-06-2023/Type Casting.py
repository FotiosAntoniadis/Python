# Type Casting = Convert the data of a value to another data type.

x = 1    # int
y = 2.0  # float
z = "3"  # str

print(x)
print(y)
print(z)

#----------------------------------------------------------------------------------------
print("---------------------------------------------------------------------------")
#----------------------------------------------------------------------------------------

# Convert "y" from float to int: int(y)
print(int(y))

# Convert "z" from str to int: str(z)
print(int(z))

